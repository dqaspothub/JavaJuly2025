package com.java.controlFlow;

public class abi {


    public static void main(String[] args) {
        int month = 1;
        switch(month)
        {
            case 1 :
                System.out.println("Jan");
                break;
            case 2 :
                System.out.println("Feb");
                break;
            case 3 :
                System.out.println("March");
                break;
            case 4 :
                System.out.println("April");
                break;
            default:
                System.out.println("Other Month");
        }


    }
}